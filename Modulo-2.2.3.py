# Operadores:

# Exponenciales ** (Si se hace una operacion con dos exponenciales Python toma priemro el exponencial que se situe al lado derecho)

print(2 ** 3)

# Ejemplo del ()
print(2 ** 2 ** 3)
print((2 ** 2) ** 3)


# Multiplicacion *

print(2 * 3)

# Division /

print(4 / 2)

# Division de Enteros //

print(4 // 2)

print(-6 // 4)
print(6. // -4)
# Ambas divisiones dan un numero decimal, por ende al ser una division de enteros el resultado se aproxima al entero mas cercano

# Restante

print(14 % 4) # El resultado es 2 ya que 14 no es divisible enteramente por 4, solo 3 veces lo que es igual a decir 4x3 = 12 es decir 14-12 = 2, 2 es el restante.

# Suma

print(-4 + 4)
print(-4. + 8)

# Resta

print(-4 - 4)
print(-4. - 8)
print(-1.1)



