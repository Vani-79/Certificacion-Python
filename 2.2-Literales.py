#Python siempre elige la presentación más corta del número, y esto se debe de tomar en consideración al crear literales. 
print(0.0000000000000000000001) # Resultado esperado: 0.0000000000000000000001
 
# Resultado Obtenido: "1e-22" ya que es la forma mas corta de expresar el numero.

